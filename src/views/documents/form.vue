<template>
  <transition name="modal">
    <widget-modal
      v-if="showModal"
      layout="icon-modal-folder"
      :title="'cadastro de documentos'"
    >
      <template v-slot:body>
        <div class="form_container">
          <div class="form_container_text">
            <fild-input
              :text="'Título'"
              v-model="value.title"
              :value="value.title"
              required
              :isError="isError && !value.title"
            />
            <fild-input
              :text="'Link'"
              v-model="value.link"
              :value="value.link"
              required
              :isError="isError && !value.link"
            />
            <fild-select
              :text="'Permissão'"
              v-model="value.permission"
              :value="value.permission"
              required
              :isError="isError && !value.permission"
              :options="getPermission"
            />

            <fild-select
              :text="'Categoria'"
              v-model="value.category"
              :value="value.category"
              required
              :isError="isError && !value.category"
              :options="teste"
            />
          </div>
        </div>
      </template>
      <template v-slot:footer>
        <send
          :isLoading="isLoading"
          :isSuccess="isSuccess"
          :isError="isError"
          @click="sendForm"
        ></send>
      </template>
    </widget-modal>
  </transition>
</template>
<script>
import FildInput from '@/components/input/Fild.vue'
import FildSelect from '@/components/input/FildSelect.vue'
import IconBase from '@/components/icons/IconBase.vue'
import Send from '@/components/button/Send.vue'
import WidgetModal from '@/components/widget/WidgetModal.vue'

import useDocument from '@/composables/useDocument'
import useCategory from '@/composables/useCategory'
import usePermission from '@/composables/usePermission'

export default {
  data() {
    return {
      isLoading: false,
      isSuccess: false,
      isError: false
    }
  },
  components: { FildInput, IconBase, Send, WidgetModal, FildSelect },
  props: {
    showModal: { type: Boolean, required: true },
    isEdit: false,
    value: {
      type: Object,
      required: false,
      default: {
        title: '',
        category: 0,
        permissions: 0,
        link: ''
      }
    }
  },
  setup() {
    const { create } = useDocument()

    const { getCategories } = useCategory()
    const { getPermission } = usePermission()

    const teste = [
      { id: 1, title: "Old Man's War" },
      { id: 2, title: 'The Lock Artist' },
      { id: 3, title: 'HTML5' },
      { id: 4, title: 'Right Ho Jeeves' },
      { id: 5, title: 'The Code of the Wooster' },
      { id: 6, title: 'Thank You Jeeves' }
    ]

    return { create, getCategories, getPermission, teste }
  },

  methods: {
    changeInput(index, value) {
      this.value[index] = typeof value == 'object' ? value.id : value
    },
    sendForm() {
      this.isLoading = true
      if (this.validForm()) {
        let result
        if (this.isEdit) {
          result = this.create(this.value)
        } else {
          result = this.create(this.value)
        }

        this.isLoading = false
        if (result) {
          this.isSuccess = true
          setTimeout(() => {
            this.isSuccess = false
            this.$router.push('/documentos')
          }, 3000)
        }
      } else {
        this.isLoading = false
        this.isError = true
        setTimeout(() => {
          this.isError = false
        }, 3000)
      }
    },
    validForm() {
      return Object.values(this.value).every((item) => !!item)
    }
  }
}
</script>

